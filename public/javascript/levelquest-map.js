let selectedLanguage = null;
const modal = document.getElementById('prologueModal');
const continents = document.querySelectorAll('.continent');

// ëŒ€ë¥™ í´ë¦­ ì´ë²¤íŠ¸
continents.forEach(continent => {
    continent.addEventListener('click', () => {
        const lang = continent.dataset.lang;
        selectedLanguage = lang;
        showPrologue(lang);
    });
});

// í”„ë¡¤ë¡œê·¸ í‘œì‹œ
function showPrologue(lang) {
    const data = prologues[lang];
    document.getElementById('prologueIcon').textContent = data.icon;
    document.getElementById('prologueTitle').textContent = data.title;
    document.getElementById('prologueSubtitle').textContent = data.subtitle;
    document.getElementById('prologueStory').textContent = data.story;
    modal.classList.add('show');
}

